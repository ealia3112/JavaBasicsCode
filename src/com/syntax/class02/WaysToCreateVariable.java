package com.syntax.class02;

public class WaysToCreateVariable {

	public static void main(String[] args) {
		// to create variable we need 2 things : datatype and name 
		
		/*
		 * 1 way to create a variable
		 * datatype name= value
		 */
		int temperature=60;
		System.out.println(temperature);
		
		/*
		 * 2nd way is done in 2 steps
		 * create a variable (datatype and name)
		 * assign the value
		 */
		
		int sum; // create a variable
		sum=30; // assign the value
		sum=50;// reasing the value
		System.out.println(sum);
		
		int num1, num2, num3; // create 3 variable of int type
		num1=10;// assign the value
		num2=20;// assign the value
		num3=30;// assign the value
		
		System.out.println (num1 + num2 + num3);
		
		
		
		
		
		
		
		
		

	}

}
